// ─────────────────────────────────────────────────────────────────────────────
// tests/unit/passkey.test.ts
//
// v1.4.11 — End-to-end tests for the WebAuthn passkey path.
//
// Strategy
// ────────
// happy-dom does not implement WebAuthn. We do NOT add a polyfill (that
// would mean we test the polyfill, not the real Web platform). Instead,
// every test:
//
//   1. Generates a real ECDSA P-256 keypair via SubtleCrypto.
//   2. Synthesises WebAuthn-shaped responses around that keypair —
//      authenticatorData, clientDataJSON, attestationObject built by hand.
//   3. Injects a fake `navigator.credentials` via __setCredentialsForTesting.
//   4. Drives the production code paths: enrolPasskey → signPayload(...,
//      {keySource: "passkey"}) → verifyEnvelope.
//
// What this proves on every CI run:
//   • The CBOR / COSE parser accepts authenticator-shaped output.
//   • The DER → raw r||s converter handles real DER signatures.
//   • The verifier replays the WebAuthn signed-bytes formula correctly.
//   • Every honesty rule (challenge binding, type=webauthn.get, no
//     silent fallback) is enforced by the verifier.
// ─────────────────────────────────────────────────────────────────────────────

import { describe, it, expect, beforeEach, afterEach } from "vitest";
